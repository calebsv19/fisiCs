unsigned probe15_crc_pick(unsigned idx) {
    unsigned base = TABLE_A[idx & 63u];
    unsigned salt = (idx + 1u) * 0x9e3779b9u;
    return mix32(base ^ salt);
}

unsigned probe15_crc_fold(unsigned state, unsigned value, unsigned round) {
    unsigned x = mix32(value + round * 131u + state * 17u);
    return (state ^ x) * 16777619u + 2166136261u;
}

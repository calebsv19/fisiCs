unsigned w25_seed_pick(unsigned idx) {
    unsigned a = TABLE_A[idx & 63u];
    unsigned b = TABLE_B[(idx * 5u + 11u) & 63u];
    return mix32(a ^ (b << 1u) ^ (idx * 0x9e3779b9u));
}

unsigned w25_seed_fold(unsigned state, unsigned value, unsigned round) {
    unsigned x = mix32(value + round * 131u + state * 17u + 0x7f4a7c15u);
    return (state ^ x) * 16777619u + 2166136261u;
}
